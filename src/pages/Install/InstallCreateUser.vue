<template>
	<div>
		<v-card>
			<v-card-title>{{ $t('install.createUser.title') }}</v-card-title>
			<v-card-text>
				<CElementCover
					v-if='running'
					:opacity='0.75'
					style='z-index: 10000;'
				>
					<CSpinner color='primary' />
				</CElementCover>
				<ValidationObserver v-slot='{invalid}'>
					<form @submit.prevent='handleSubmit'>
						<div class='form-group'>
							{{ $t('install.createUser.note') }}
						</div>
						<ValidationProvider
							v-slot='{ valid, touched, errors }'
							rules='required'
							:custom-messages='{
								required: $t("forms.errors.username"),
							}'
						>
							<v-text-field
								id='username'
								v-model='username'
								:label='$t("forms.fields.username")'
								:success='touched ? valid : null'
								:error-messages='errors'
							/>
						</ValidationProvider>
						<ValidationProvider
							v-slot='{ valid, touched, errors }'
							rules='email'
							:custom-messages='{
								email: $t("forms.errors.emailFormat"),
							}'
						>
							<v-text-field
								id='email'
								v-model='email'
								:label='$t("forms.fields.email")'
								:success='touched ? valid : null'
								:error-messages='errors'
							/>
						</ValidationProvider>
						<ValidationProvider
							v-slot='{valid, touched, errors}'
							rules='required'
							:custom-messages='{
								required: $t("forms.errors.password"),
							}'
						>
							<v-text-field
								id='password'
								v-model='password'
								:label='$t("forms.fields.password")'
								:success='touched ? valid : null'
								:error-messages='errors'
								:type='passwordVisible ? "text" : "password"'
								:append-icon='passwordVisible ? "mdi-eye" : "mdi-eye-off"'
								@click:append='passwordVisible = !passwordVisible'
							/>
						</ValidationProvider>
						<v-btn color='primary' type='submit' :disabled='invalid'>
							{{ $t('install.createUser.createButton') }}
						</v-btn>
					</form>
				</ValidationObserver>
			</v-card-text>
		</v-card>
	</div>
</template>

<script lang='ts'>
import {Component, Vue} from 'vue-property-decorator';
import {CElementCover, CSpinner} from '@coreui/vue/src';
import {extend, ValidationObserver, ValidationProvider} from 'vee-validate';

import UserService from '@/services/UserService';

import {extendedErrorToast} from '@/helpers/errorToast';
import {email, required} from 'vee-validate/dist/rules';
import {sleep} from '@/helpers/sleep';
import {UserCredentials, UserRole} from '@/services/AuthenticationService';

import {AxiosError, AxiosResponse} from 'axios';

@Component({
	components: {
		CElementCover,
		CSpinner,
		ValidationObserver,
		ValidationProvider,
	},
	metaInfo: {
		title: 'install.createUser.title',
	},
})

/**
 * Create user
 */
export default class InstallCreateUser extends Vue {

	/**
	 * Email
	 */
	private email = '';

	/**
	 * User name
	 */
	private username = '';

	/**
	 * User password
	 */
	private password = '';

	/**
	 * @var {bool} passwordVisible Controls visibility of password field
	 */
	private passwordVisible = false;

	/**
	 * User language
	 */
	private language = 'en';

	/**
	 * User role
	 */
	private role = UserRole.ADMIN;

	/**
	 * @var {bool} running Indicates whether axios requests are in progress
	 */
	private running = false;

	/**
	 * On component creation event handler
	 */
	public created(): void {
		extend('email', email);
		extend('required', required);
	}

	/**
	 * Handle form submit
	 */
	private handleSubmit(): void {
		this.running = true;
		UserService.add(this.username, this.email, this.password, this.language, this.role)
			.then((response: AxiosResponse) => {
				if (response.data.emailSent === true) {
					this.$toast.success(
						this.$t('core.user.messages.verifyNotice').toString()
					);
				}
				const credentials = new UserCredentials(this.username, this.password);
				this.$store.dispatch('user/signIn', credentials)
					.then(async () => {
						await sleep(500);
						this.running = false;
						this.nextStep();
					});
			})
			.catch((error: AxiosError) => {
				extendedErrorToast(error, 'install.createUser.failure');
				this.running = false;
			});
	}

	/**
	 * Advances the installation wizard
	 */
	private nextStep(): void {
		this.$emit('next-step');
		this.$router.push('/install/smtp/');
	}
}
</script>
